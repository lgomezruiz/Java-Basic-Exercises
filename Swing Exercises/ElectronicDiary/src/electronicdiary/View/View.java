    private void btnRightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRightActionPerformed
        if (Integer.parseInt(this.txtIndex.getText()) < 9) {
            this.txtIndex.setText(String.valueOf(Integer.parseInt(this.txtIndex.getText()) + 1));
            this.txtDni.setText(dni[Integer.parseInt(this.txtIndex.getText())]);
            this.txtName.setText(name[Integer.parseInt(this.txtIndex.getText())]);
            this.txtLastName.setText(lastName[Integer.parseInt(this.txtIndex.getText())]);
            this.txtAddress.setText(address[Integer.parseInt(this.txtIndex.getText())]);
            this.txtPhone.setText(phone[Integer.parseInt(this.txtIndex.getText())]);
            this.txtBirthdate.setText(birthdate[Integer.parseInt(this.txtIndex.getText())]);
        }
    }//GEN-LAST:event_btnRightActionPerformed

    private void btnLeftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeftActionPerformed
        if (Integer.parseInt(this.txtIndex.getText()) > 0) {
            this.txtIndex.setText(String.valueOf(Integer.parseInt(this.txtIndex.getText()) - 1));
            this.txtDni.setText(dni[Integer.parseInt(this.txtIndex.getText())]);
            this.txtName.setText(name[Integer.parseInt(this.txtIndex.getText())]);
            this.txtLastName.setText(lastName[Integer.parseInt(this.txtIndex.getText())]);
            this.txtAddress.setText(address[Integer.parseInt(this.txtIndex.getText())]);
            this.txtPhone.setText(phone[Integer.parseInt(this.txtIndex.getText())]);
            this.txtBirthdate.setText(birthdate[Integer.parseInt(this.txtIndex.getText())]);
        }
    }//GEN-LAST:event_btnLeftActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        dni[Integer.parseInt(this.txtIndex.getText())] = this.txtDni.getText();
        name[Integer.parseInt(this.txtIndex.getText())] = this.txtName.getText();
        lastName[Integer.parseInt(this.txtIndex.getText())] = this.txtLastName.getText();
        address[Integer.parseInt(this.txtIndex.getText())] = this.txtAddress.getText();
        phone[Integer.parseInt(this.txtIndex.getText())] = this.txtPhone.getText();
        birthdate[Integer.parseInt(this.txtIndex.getText())] = this.txtBirthdate.getText();
    }//GEN-LAST:event_btnSaveActionPerformed

